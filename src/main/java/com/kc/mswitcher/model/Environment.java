package com.kc.mswitcher.model;

public class Environment {

	private String env;
	private String dnsValue;
	private String maintDnsValue;

	public String getEnv() {
		return env;
	}

	public void setEnv(String env) {
		this.env = env;
	}

	public String getDnsValue() {
		return dnsValue;
	}

	public void setDnsValue(String dnsValue) {
		this.dnsValue = dnsValue;
	}

	public String getMaintDnsValue() {
		return maintDnsValue;
	}

	public void setMaintDnsValue(String maintDnsValue) {
		this.maintDnsValue = maintDnsValue;
	}

}
